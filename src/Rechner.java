public class Rechner {




    public int addieren (int a, int b){

      return a+b;
    }


    public double addieren (double a, double b){

        return a+b;
    }

    public int addieren (int a, int b, int c){
        return a+b+c;
    }

    public double addieren (double a, double b, double c){

        return a+b+c;
    }
}
